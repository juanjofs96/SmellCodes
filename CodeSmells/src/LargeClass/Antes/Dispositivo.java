public class Dispositivo{
    private int num;
    private int medida;
    private String descripcion;
    private String huella;
    private String inteligencia;
    private String chip;
    private String telefonica;

    public Dispositivo(int num, int medida, String descripcion, String huella, String inteligencia, String chip, String telefonica) {
        this.num = num;
        this.medida = medida;
        this.descripcion = descripcion;
        this.huella = huella;
        this.inteligencia = inteligencia;
        this.chip = chip;
        this.telefonica = telefonica;
    }


}
